#  operasi komparasi 

# setiap hasil dari komparasi adalah boolean

# >, <, >=, <=, ==, !=, is, is not

x = 5
y = 1

# lebih dari (>)
hasil = x > 2
print(x, ">", 2, "=", hasil )
hasil = y > 2
print(y, ">", 2, "=", hasil )
hasil = y > 6
print(x, ">", 6, "=", hasil )

# kurang dari (<)
print("=" * 10) 
hasil = x < 2
print(x, "<", 2, "=", hasil )
hasil = y < 2
print(y, "<", 2, "=", hasil )
hasil = y < 6
print(y, "<", 6, "=", hasil )

# kurang dari (<)
print("=" * 10  ) 
hasil = x < 2
print(x, "<", 2, "=", hasil )
hasil = y < 2
print(y, "<", 2, "=", hasil )
hasil = y < 6
print(y, "<", 6, "=", hasil )

# kurang atau sama dengan dari (<=)
print("=" * 10  ) 
hasil = x <= 2
print(x, "<=", 2, "=", hasil )
hasil = y <= 2
print(y, "<=", 2, "=", hasil )
hasil = y <= 1
print(y, "<=", 1, "=", hasil )

# lebih atau sama dengan dari (>=)
print("=" * 10  ) 
hasil = x >= 2
print(x, ">=", 2, "=", hasil )
hasil = y >= 2
print(y, ">=", 2, "=", hasil )
hasil = y >= 1
print(y, ">=", 1, "=", hasil )

#  sama dengan (==)
print("=" * 10  ) 
hasil = x == 2
print(x, "==", 2, "=", hasil )
hasil = y == 1
print(y, "==", 1, "=", hasil )

#  tidak sama dengan (!=)
print("=" * 10  ) 
hasil = x != 2
print(x, "!=", 2, "=", hasil )
hasil = y != 1
print(y, "!=", 1, "=", hasil )